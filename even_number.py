# Nth Even Number

# Challenge (Easy)

# Write a program that gets an integer n from input and outputs the nth even number starting from 0.

# Examples:

# Input: 1, Output: 0 (0 is the first even number)
# Input: 2, Output: 2 (2 is the second even number)
# Input: 100, Output: 198

#####################################################################################################

# Write code here
n = int(input())

even_number = (n - 1) * 2

print(even_number)